

-- COUNT BOOKMARKS IN THE BOOKMARK TABLE
GET_BOOKMARK_COUNT
{
	SELECT COUNT(*) FROM BOOKMARK
}

-- INSERT ROW INTO BOOKMARK
INSERT_BOOKMARK
{
	INSERT INTO BOOKMARK (DOCUMENTID) 
	VALUES (?)
}

-- IS BOOKMARK IN TABLE
GET_BOOKMARK_DOCUMENTID
{
	SELECT DOCUMENTID FROM BOOKMARK
	WHERE DOCUMENTID = '[DOCUMENTID]'
}

-- DELETE A BOOKMARK
DELETE_BOOKMARK
{
	DELETE FROM BOOKMARK
	WHERE DOCUMENTID = ?
}

-- GET ALL BOOKMARK DOCUMENTS
GET_ALL_BOOKMARKS
{
	SELECT DOCUMENTID FROM BOOKMARK
}
